func TestProblem(t *testing.T) {
	for _, test := range TestCases {
		actual := sampleStats(test.input)

		var result = true
		for i := 0; i <= 4; i++ {
			tmp := actual[i] - test.results[i]

			if tmp > 0.00001 || tmp < 0.00001 {
				result = false
				break
			}
		}

		if result == false {
			t.Logf(MessageOk, test.results)
		} else {
			t.Errorf(MessageError, test.results, actual)
		}
	}
}

